import java.util.*;

public class conditionalstatement {
    public static void main(String[] args) {
        Scanner sc =new Scanner(System.in);
        int age = sc.nextInt();
        // IF AND ELSE CONDITIONAL STATEMENT 
        if (age>18) {
            System.out.println("adult");
        } else{
            System.out.println("not adult");
        }
    }
    
}
